#include "../../include/minishell.h"

int	ft_pwd(void)
{
	char	buf[PATH_LEN];

	if (!getcwd(buf, PATH_LEN))
		return (ERROR);
	ft_putendl_fd(buf, STDOUT);
	return (0);
}
